     */

    public function store(Request $request,$id)
    {
        $product= Product::find($id);
        $noti=new Notification();
        $noti->buyer_id=auth()->user()->id;
        $noti->seller_id=$product->user_id;
        $noti->product_id=$product->id;
        $noti->save();
    }

